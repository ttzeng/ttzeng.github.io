function getSpeechGreeting() {
	var now = new Date();
	var weekdays = new Array('Sunday','Monday','Tuesday','Wednesday','Thursday','Friday','Saturday');
	var months = new Array('January','February','March','April','May','June','July','August','September','October','November','December');
	var today = months[now.getMonth()] + ' ' + now.getDate() + ' ' + weekdays[now.getDay()] + ', ' + (1900+now.getYear());
	var greeting = 'http://translate.google.com/translate_tts?tl=en&q="Today is ' + today + '"';
	var element = "<embed width=0 height=0 loop='false' hidden='false' autostart='true' src='" + greeting + "' />";
	return element;
}

function getMyGlobalIp(cbSuccess, cbComplete, cbError) {
	var jsoninfo = $.ajax({
		url: "http://jsonip.com/?callback=?",
		type: "GET",
		dataType: "jsonp",
		success: function(jsondata) { if (cbSuccess!=null) cbSuccess(jsondata.ip) },
		error: function(request, error) { if (cbError) cbError(request, error) },
		complete: function(jsoninfo, status) { if (cbComplete) cbComplete(jsoninfo, status) }
	});
};

